/**
 * op_add - add two numbers a and b
 * @a: the first input
 * @b: the second input
 * Return: returns the sum of a and b
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - substract two numbers a and b
 * @a: the first input
 * @b: the second input
 * Return: returns the difference of a and b
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiples two numbers a and b
 * @a: the first input
 *@b: the second input
 * Return: returns the product of a and b
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divides two numbers a and b
 * @a: the first input
 * @b: the second input
 * Return: returns the result of the division of a by b
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - finds the a modulus b
 * @a: the first input
 * @b: the second input
 * Return: the remainder of the division of a by b
 */

int op_mod(int a, int b)
{
	return (a % b);
}
